package org.nograph.util;

import java.io.StringWriter;

public class StringUtil 
{
    public static String escapeJSON(String str)
    {
    	if(str == null || str.length() == 0) return str;
    	
    	int len = str.length();
        char b;
        char c = 0;
        String hhhh;
    	StringWriter w = new StringWriter((int)(1.1*len));
        for (int i = 0; i < len; i += 1) {
            b = c;
            c = str.charAt(i);
            switch (c) {
            case '\\':
            case '"':
                w.write('\\');
                w.write(c);
                break;
            case '/':
                if (b == '<') {
                    w.write('\\');
                }
                w.write(c);
                break;
            case '\b':
                w.write("\\b");
                break;
            case '\t':
                w.write("\\t");
                break;
            case '\n':
                w.write("\\n");
                break;
            case '\f':
                w.write("\\f");
                break;
            case '\r':
                w.write("\\r");
                break;
            default:
                if (c < ' ' || (c >= '\u0080' && c < '\u00a0')
                        || (c >= '\u2000' && c < '\u2100')) {
                    w.write("\\u");
                    hhhh = Integer.toHexString(c);
                    w.write("0000", 0, 4 - hhhh.length());
                    w.write(hhhh);
                } else {
                    w.write(c);
                }
            }
        }
        
        return w.toString();
    }
}
